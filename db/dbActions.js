exports.findDocuments = function(db, collectionName, model){
    return new Promise(function(resolve, reject){
      var collection = db.collection(collectionName);
      // Find some documents
      collection.find(model).toArray(function(err, docs) {
        if (err) reject(err)
        else resolve(docs)
      });
    }) 
}

